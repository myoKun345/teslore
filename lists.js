$(document).ready
(
	function()
	{
		$('div.expando').click
		(
			function()
			{
				$(this).addClass
				(
					'clicked'
				);
				$('div.expando.clicked + ul.list').slideToggle
				(
					'slow'
				);
				$(this).toggleClass
				(
					'expanded'
				);
				$(this).removeClass
				(
					'clicked'
				);
			}
		);
	}
);
